# Input nilai variabel
a = input("Masukkan nilai a:8 ")
b = input("Masukkan nilai b:10 ")

# Konversi input ke tipe data integer
a = int(a)
b = int(b)

# Cetak nilai variabel
print("Variabel a =", a)
print("Variabel b =", b)

# Cetak nilai operasi kedua variabel dengan String Format
print("Hasil penggabung {}&{} = {}".format(a, b, a + b))

# Hasil penjumlahan
print("Hasil penjumlahan {} + {} = {}".format(a, b, a + b))

# Hasil pembagian
print("Hasil pembagian {} / {} = {}".format(a, b, a / b))